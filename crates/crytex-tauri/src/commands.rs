#[async_trait]
impl InferenceService for AuditedInferenceService {
    async fn generate(
        &self,
        request: InferenceRequest,
    ) -> Result<InferenceResponse, crytex_core::services::InferenceServiceError> {
        let started = Instant::now();
        self.scope
            .log(
                "llm_request",
                AuditLogLevel::Info,
                serde_json::json!({
                    "backend_id": request.backend_id,
                    "model": request.model,
                    "message_count": request.messages.len(),
                    "lora_adapter_id": request.lora_adapter_id,
                    "temperature": request.temperature,
                    "max_tokens": request.max_tokens,
                    "messages": request.messages,
                }),
            )
            .await;

        let result = self.inner.generate(request).await;
        match &result {
            Ok(response) => {
                self.scope
                    .log(
                        "llm_response",
                        AuditLogLevel::Info,
                        serde_json::json!({
                            "finish_reason": response.finish_reason,
                            "usage": {
                                "prompt_tokens": response.usage.prompt_tokens,
                                "completion_tokens": response.usage.completion_tokens,
                                "total_tokens": response.usage.total_tokens,
                            },
                            "content": response.content,
                            "duration_ms": started.elapsed().as_millis() as u64,
                        }),
                    )
                    .await;
            }
            Err(error) => {
                self.scope
                    .log(
                        "llm_error",
                        AuditLogLevel::Error,
                        serde_json::json!({
                            "error": error.to_string(),
                            "duration_ms": started.elapsed().as_millis() as u64,
                        }),
                    )
                    .await;
            }
        }
        result
    }

    async fn embed(
        &self,
        text: &str,
    ) -> Result<Vec<f32>, crytex_core::services::InferenceServiceError> {
        InferenceService::embed(self.inner.as_ref(), text).await
    }

    fn available_backends(&self) -> Vec<BackendInfo> {
        self.inner.available_backends()
    }

    async fn register_lora(
        &self,
        lora: LoRAAdapter,
    ) -> Result<(), crytex_core::services::InferenceServiceError> {
        self.inner.register_lora(lora).await
    }

    async fn swap_lora(
        &self,
        lora_id: &str,
    ) -> Result<(), crytex_core::services::InferenceServiceError> {
        self.inner.swap_lora(lora_id).await
    }

    async fn list_models(
        &self,
        backend_id: Option<&str>,
    ) -> Result<Vec<ModelInfo>, crytex_core::services::InferenceServiceError> {
        self.inner.list_models(backend_id).await
    }
}

