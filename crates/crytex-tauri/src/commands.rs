fn diagnostic_trace_ids(
    request: &ExportRunDiagnosticsCommand,
    events: &[RunDiagnosticEvent],
) -> Vec<String> {
    let mut trace_ids = request.trace_id.iter().cloned().collect::<BTreeSet<_>>();
    trace_ids.extend(
        events
            .iter()
            .filter_map(|event| event.metadata["trace_id"].as_str().map(ToOwned::to_owned)),
    );
    trace_ids.into_iter().collect()
}

fn diagnostic_review_task_ids(
    tasks: &[RunDiagnosticTask],
    events: &[RunDiagnosticEvent],
) -> Vec<String> {
    let mut ids = tasks
        .iter()
        .filter(|task| task.status == "Review")
        .map(|task| task.id.clone())
        .collect::<Vec<_>>();
    for id in events
        .iter()
        .flat_map(|event| {
            event.metadata["review_task_ids"]
                .as_array()
                .into_iter()
                .flatten()
        })
        .filter_map(Value::as_str)
    {
        if !ids.iter().any(|existing| existing == id) {
            ids.push(id.to_string());
        }
    }
    ids
}

fn diagnostic_lora_evolution(events: &[RunDiagnosticEvent]) -> Vec<RunDiagnosticLoraEvolution> {
    events
        .iter()
        .filter_map(|event| {
            if !event.action.starts_with("lora_evolution_") {
                return None;
            }
            let gate = event
                .metadata
                .get("benchmark_gate")
                .or_else(|| event.metadata.get("lora_benchmark_gate"));
            Some(RunDiagnosticLoraEvolution {
                task_id: event.task_id.clone(),
                training_job_id: event
                    .metadata
                    .get("training_job_id")
                    .and_then(Value::as_str)
                    .map(ToOwned::to_owned),
                adapter_id: event
                    .metadata
                    .get("adapter_id")
                    .or_else(|| event.metadata.get("promoted_adapter_id"))
                    .or_else(|| event.metadata.get("lora_id"))
                    .and_then(Value::as_str)
                    .map(ToOwned::to_owned),
                accepted: gate
                    .and_then(|gate| gate.get("accepted"))
                    .and_then(Value::as_bool)
                    .or(match event.action.as_str() {
                        "lora_evolution_promoted" => Some(true),
                        "lora_evolution_rejected" => Some(false),
                        _ => None,
                    }),
                reason: gate
                    .and_then(|gate| gate.get("reason"))
                    .or_else(|| event.metadata.get("reason"))
                    .and_then(Value::as_str)
                    .map(ToOwned::to_owned),
                baseline_run_id: gate
                    .and_then(|gate| gate.get("baseline_run_id"))
                    .and_then(Value::as_str)
                    .map(ToOwned::to_owned),
                challenger_run_id: gate
                    .and_then(|gate| gate.get("challenger_run_id"))
                    .and_then(Value::as_str)
                    .map(ToOwned::to_owned),
                winner: gate
                    .and_then(|gate| gate.get("winner"))
                    .and_then(Value::as_str)
                    .map(ToOwned::to_owned),
                mc_nemar_p_value: gate
                    .and_then(|gate| gate.get("mc_nemar_p_value"))
                    .and_then(Value::as_f64),
                baseline_pass_rate: gate
                    .and_then(|gate| gate.get("baseline_pass_rate"))
                    .and_then(Value::as_f64),
                challenger_pass_rate: gate
                    .and_then(|gate| gate.get("challenger_pass_rate"))
                    .and_then(Value::as_f64),
                metadata: event.metadata.clone(),
            })
        })
        .collect()
}

