/// Start a minimal local run for UI smoke testing.
///
/// This does not pretend that real agent work completed. It attaches a
/// deterministic stub result and moves tasks to review so humans can see the
/// smoke output without corrupting completion/evolution signals.
pub async fn start_run(
    task_service: Arc<dyn TaskService>,
    executor: Arc<dyn TaskExecutor>,
    request: StartRunCommand,
) -> Result<StartRunResponse, TauriCommandError> {
    start_run_with_orchestrator(task_service, None, None, None, executor, request).await
}

/// Start a local run and let the orchestrator create remediation work after critic rejections.
pub async fn start_run_with_orchestrator(
    task_service: Arc<dyn TaskService>,
    orchestrator: Option<Arc<dyn Orchestrator>>,
    audit_service: Option<Arc<dyn AuditLogService>>,
    event_service: Option<Arc<dyn EventService>>,
    executor: Arc<dyn TaskExecutor>,
    request: StartRunCommand,
) -> Result<StartRunResponse, TauriCommandError> {
    let started_at = unix_timestamp_millis();
    let run_id = format!("run-{started_at}");
    let max_steps = request.max_steps.max(1);
    let mut review_tasks = Vec::new();
    let mut run_started_logged = false;

    for _ in 0..max_steps {
        let Some(mut task) =
            next_project_ready_task(task_service.clone(), &request.project_id).await?
        else {
            break;
        };
        if !run_started_logged {
            log_run_event(
                audit_service.as_ref(),
                event_service.as_ref(),
                "run_started",
                Some(&task),
                &task.project_id,
                Some(&task.trace_id),
                serde_json::json!({
                    "run_id": run_id,
                    "max_steps": max_steps,
                }),
            )
            .await;
            run_started_logged = true;
        }

        let handoff_result = attach_upstream_artifacts(
            task_service.clone(),
            audit_service.as_ref(),
            event_service.as_ref(),
            &run_id,
            &mut task,
        )
        .await?;
        task_service.update_task(&task).await?;
        if handoff_result.rejected_count > 0 {
            continue;
        }
        let running = task_service
            .set_status(&task.id, TaskStatus::InProgress)
            .await?;
        log_run_event(
            audit_service.as_ref(),
            event_service.as_ref(),
            "task_execution_started",
            Some(&running),
            &running.project_id,
            Some(&running.trace_id),
            serde_json::json!({
                "run_id": run_id,
                "kind": running.kind,
                "agent": running.assigned_agent,
            }),
        )
        .await;
        let result = match executor.execute(&running, &run_id).await {
            Ok(result) => result,
            Err(error) => {
                let mut failed = running;
                failed.result = Some(serde_json::json!({
                    "source": "task_executor_error",
                    "run_id": run_id,
                    "task_id": failed.id,
                    "agent": failed.assigned_agent,
                    "kind": failed.kind,
                    "error": error.to_string(),
                }));
                task_service.update_task(&failed).await?;
                let failed = task_service
                    .set_status(&failed.id, TaskStatus::Failed)
                    .await?;
                log_run_event(
                    audit_service.as_ref(),
                    event_service.as_ref(),
                    "task_execution_failed",
                    Some(&failed),
                    &failed.project_id,
                    Some(&failed.trace_id),
                    serde_json::json!({
                        "run_id": run_id,
                        "kind": failed.kind,
                        "agent": failed.assigned_agent,
                        "result_source": "task_executor_error",
                        "error": error.to_string(),
                    }),
                )
                .await;
                break;
            }
        };
        let mut task_with_result = running;
        let (result, contract_repaired) = repair_agent_contract_result(&task_with_result, result);
        task_with_result.result = Some(result);
        if contract_repaired {
            log_run_event(
                audit_service.as_ref(),
                event_service.as_ref(),
                "agent_contract_repaired",
                Some(&task_with_result),
                &task_with_result.project_id,
                Some(&task_with_result.trace_id),
                serde_json::json!({
                    "run_id": run_id,
                    "agent": task_with_result.assigned_agent,
                    "kind": task_with_result.kind,
                    "artifact_kind": artifact_kind_for_task(&task_with_result),
                    "repair_strategy": "raw_content_to_typed_agent_result",
                }),
            )
            .await;
        }
        if let Err(contract_rejection) = validate_executed_agent_contract(&task_with_result) {
            handle_agent_contract_rejection(
                task_service.clone(),
                audit_service.clone(),
                event_service.clone(),
                &run_id,
                task_with_result,
                contract_rejection,
            )
            .await?;
            continue;
        }
        task_service.update_task(&task_with_result).await?;
        log_run_event(
            audit_service.as_ref(),
            event_service.as_ref(),
            "task_execution_finished",
            Some(&task_with_result),
            &task_with_result.project_id,
            Some(&task_with_result.trace_id),
            serde_json::json!({
                "run_id": run_id,
                "kind": task_with_result.kind,
                "agent": task_with_result.assigned_agent,
                "result_source": task_with_result
                    .result
                    .as_ref()
                    .and_then(|result| result.get("source"))
                    .and_then(|source| source.as_str()),
            }),
        )
        .await;

        if is_reviewer_task(&task_with_result) {
            let decision = review_decision(&task_with_result);
            if decision == Some("reject") {
                log_run_event(
                    audit_service.as_ref(),
                    event_service.as_ref(),
                    "critic_rejected",
                    Some(&task_with_result),
                    &task_with_result.project_id,
                    Some(&task_with_result.trace_id),
                    serde_json::json!({
                        "run_id": run_id,
                        "feedback": rejection_feedback(&task_with_result),
                        "target_task_id": rejection_target_task_id(&task_with_result),
                        "failure_type": critic_report(&task_with_result)
                            .and_then(|report| report.get("failure_type"))
                            .and_then(|failure_type| failure_type.as_str()),
                    }),
                )
                .await;
                if should_escalate_reviewer_rejection(task_service.clone(), &task_with_result)
                    .await?
                {
                    let review_task = task_service
                        .set_status(&task_with_result.id, TaskStatus::Review)
                        .await?;
                    log_run_event(
                        audit_service.as_ref(),
                        event_service.as_ref(),
                        "human_review_ready",
                        Some(&review_task),
                        &review_task.project_id,
                        Some(&review_task.trace_id),
                        serde_json::json!({
                            "run_id": run_id,
                            "review_task_ids": [review_task.id.clone()],
                            "escalated_reviewer_rejection": true,
                            "feedback": rejection_feedback(&review_task),
                        }),
                    )
                    .await;
                    review_tasks.push(review_task);
                    break;
                }
                if let Some(orchestrator) = orchestrator.clone() {
                    create_reviewer_remediation_plan(
                        task_service.clone(),
                        orchestrator,
                        audit_service.clone(),
                        event_service.clone(),
                        &run_id,
                        &task_with_result,
                    )
                    .await?;
                } else {
                    handle_reviewer_rejection(task_service.clone(), &task_with_result).await?;
                }
                continue;
            }
            let review_task = task_service
                .set_status(&task_with_result.id, TaskStatus::Review)
                .await?;
            log_run_event(
                audit_service.as_ref(),
                event_service.as_ref(),
                "human_review_ready",
                Some(&review_task),
                &review_task.project_id,
                Some(&review_task.trace_id),
                serde_json::json!({
                    "run_id": run_id,
                    "review_task_ids": [review_task.id],
                }),
            )
            .await;
            review_tasks.push(review_task);
        } else if is_generated_chain_task(&task_with_result) {
            task_service
                .set_status(&task_with_result.id, TaskStatus::Completed)
                .await?;
        } else {
            let review_task = task_service
                .set_status(&task_with_result.id, TaskStatus::Review)
                .await?;
            log_run_event(
                audit_service.as_ref(),
                event_service.as_ref(),
                "human_review_ready",
                Some(&review_task),
                &review_task.project_id,
                Some(&review_task.trace_id),
                serde_json::json!({
                    "run_id": run_id,
                    "review_task_ids": [review_task.id],
                }),
            )
            .await;
            review_tasks.push(review_task);
        }
    }

    let remaining_ready_tasks = project_ready_tasks(task_service, &request.project_id).await?;
    Ok(StartRunResponse {
        run_id,
        project_id: request.project_id,
        started_at,
        review_tasks,
        remaining_ready_tasks,
    })
}

const MAX_AGENT_CONTRACT_RETRIES: u32 = 1;

async fn handle_agent_contract_rejection(
    task_service: Arc<dyn TaskService>,
    audit_service: Option<Arc<dyn AuditLogService>>,
    event_service: Option<Arc<dyn EventService>>,
    run_id: &str,
    mut task: Task,
    rejection: ArtifactHandoffRejection,
) -> Result<(), TauriCommandError> {
    let reason = rejection.reason;
    task.result = Some(serde_json::json!({
        "source": "agent_contract_violation",
        "run_id": run_id,
        "task_id": task.id,
        "agent": task.assigned_agent,
        "kind": task.kind,
        "artifact_kind": rejection.artifact_kind,
        "reason": reason,
        "malformed_result": task.result,
    }));
    task_service.update_task(&task).await?;

    if task.iteration_count < MAX_AGENT_CONTRACT_RETRIES {
        let mut retried = task;
        retried.status = TaskStatus::Pending;
        retried.iteration_count += 1;
        retried.started_at = None;
        retried.finished_at = None;
        append_retry_feedback(&mut retried.payload, retried.iteration_count, &reason);
        task_service.update_task(&retried).await?;
        log_run_event(
            audit_service.as_ref(),
            event_service.as_ref(),
            "agent_contract_retry",
            Some(&retried),
            &retried.project_id,
            Some(&retried.trace_id),
            serde_json::json!({
                "run_id": run_id,
                "artifact_kind": rejection.artifact_kind,
                "reason": reason,
                "iteration_count": retried.iteration_count,
            }),
        )
        .await;
        return Ok(());
    }

    let failed = task_service
        .set_status(&task.id, TaskStatus::Failed)
        .await?;
    log_run_event(
        audit_service.as_ref(),
        event_service.as_ref(),
        "agent_contract_failed",
        Some(&failed),
        &failed.project_id,
        Some(&failed.trace_id),
        serde_json::json!({
            "run_id": run_id,
            "artifact_kind": rejection.artifact_kind,
            "reason": reason,
            "iteration_count": failed.iteration_count,
        }),
    )
    .await;
    Ok(())
}

fn append_retry_feedback(payload: &mut Value, iteration: u32, comment: &str) {
    let entry = serde_json::json!({
        "iteration": iteration,
        "comment": comment,
        "timestamp": unix_timestamp_millis(),
    });
    if let Some(items) = payload
        .get_mut("retry_feedback")
        .and_then(Value::as_array_mut)
    {
        items.push(entry);
    } else {
        payload["retry_feedback"] = serde_json::json!([entry]);
    }
}

fn repair_agent_contract_result(task: &Task, result: Value) -> (Value, bool) {
    if !requires_agent_artifact_contract(task) || agent_contract_result_is_valid(task, &result) {
        return (result, false);
    }

    let Some(raw_content) = result
        .get("content")
        .and_then(Value::as_str)
        .filter(|content| !content.trim().is_empty())
        .map(ToOwned::to_owned)
    else {
        return (result, false);
    };

    let mut repaired = result;
    repaired["agent_result"] = repaired_agent_result_for(task, &raw_content);
    repaired["contract_repair"] = serde_json::json!({
        "strategy": "raw_content_to_typed_agent_result",
        "artifact_kind": artifact_kind_for_task(task),
    });
    (repaired, true)
}

fn agent_contract_result_is_valid(task: &Task, result: &Value) -> bool {
    build_agent_artifact_envelope_for(task, result)
        .is_some_and(|envelope| validate_agent_artifact_envelope(&envelope).is_ok())
}

fn repaired_agent_result_for(task: &Task, raw_content: &str) -> Value {
    match task.assigned_agent.as_deref() {
        Some("architect") => serde_json::json!({
            "summary": raw_content,
            "content": raw_content,
            "evidence": raw_content_evidence(raw_content),
        }),
        Some("coder") => serde_json::json!({
            "summary": raw_content,
            "files_changed": inferred_files_changed(task),
            "evidence": raw_content_evidence(raw_content),
        }),
        Some("qa") => serde_json::json!({
            "summary": raw_content,
            "test_results": raw_content,
            "evidence": raw_content_evidence(raw_content),
        }),
        Some("security") => serde_json::json!({
            "summary": raw_content,
            "risk": raw_content,
            "evidence": raw_content_evidence(raw_content),
        }),
        Some("critic") => serde_json::json!({
            "review_decision": "pass",
            "summary": raw_content,
            "evidence": raw_content_evidence(raw_content),
        }),
        _ => serde_json::json!({
            "summary": raw_content,
            "evidence": raw_content_evidence(raw_content),
        }),
    }
}

fn raw_content_evidence(raw_content: &str) -> Value {
    serde_json::json!({
        "kind": "llm_raw_content",
        "content": raw_content,
    })
}

fn inferred_files_changed(task: &Task) -> Vec<Value> {
    ["files_changed", "required_files", "expected_files"]
        .into_iter()
        .find_map(|field| {
            task.payload
                .get(field)
                .and_then(Value::as_array)
                .map(|items| {
                    items
                        .iter()
                        .filter_map(Value::as_str)
                        .filter(|item| !item.trim().is_empty())
                        .map(|item| Value::String(item.to_string()))
                        .collect::<Vec<_>>()
                })
        })
        .filter(|items| !items.is_empty())
        .or_else(|| {
            task.payload
                .get("required_file")
                .and_then(Value::as_str)
                .filter(|item| !item.trim().is_empty())
                .map(|item| vec![Value::String(item.to_string())])
        })
        .unwrap_or_else(|| vec![Value::String("llm://raw-content".to_string())])
}

fn validate_executed_agent_contract(task: &Task) -> Result<(), ArtifactHandoffRejection> {
    if !requires_agent_artifact_contract(task) {
        return Ok(());
    }
    build_agent_artifact_handoff_result(task).map(|_| ())
}

fn requires_agent_artifact_contract(task: &Task) -> bool {
    matches!(
        task.assigned_agent.as_deref(),
        Some("architect" | "coder" | "qa" | "security" | "critic")
    )
}

async fn should_escalate_reviewer_rejection(
    task_service: Arc<dyn TaskService>,
    reviewer: &Task,
) -> Result<bool, TauriCommandError> {
    let Some(parent_id) = reviewer.parent_id.as_deref() else {
        return Ok(false);
    };
    let Some(parent) = task_service.get(parent_id).await? else {
        return Ok(false);
    };
    Ok(parent
        .payload
        .get("source")
        .and_then(|value| value.as_str())
        == Some("reviewer_rejection"))
}

async fn create_reviewer_remediation_plan(
    task_service: Arc<dyn TaskService>,
    orchestrator: Arc<dyn Orchestrator>,
    audit_service: Option<Arc<dyn AuditLogService>>,
    event_service: Option<Arc<dyn EventService>>,
    run_id: &str,
    reviewer: &Task,
) -> Result<Vec<Task>, TauriCommandError> {
    let feedback = rejection_feedback(reviewer).to_string();
    let critic_report = critic_report(reviewer).cloned().unwrap_or(Value::Null);
    let target_task_id = rejection_target_task_id(reviewer).map(str::to_string);
    let failure_type = critic_report
        .get("failure_type")
        .and_then(|value| value.as_str())
        .unwrap_or("review_rejection")
        .to_string();

    let reviewer = task_service
        .set_status(&reviewer.id, TaskStatus::Failed)
        .await?;
    let remediation = task_service
        .submit(CreateTaskRequest {
            project_id: reviewer.project_id.clone(),
            parent_id: reviewer.parent_id.clone(),
            title: format!("Remediate reviewer rejection: {}", reviewer.title),
            description: Some(feedback.clone()),
            kind: "debug".to_string(),
            assigned_agent: Some("architect".to_string()),
            priority: reviewer.priority + 1,
            payload: serde_json::json!({
                "source": "reviewer_rejection",
                "reviewer_task_id": reviewer.id,
                "target_task_id": target_task_id,
                "failure_type": failure_type,
                "feedback": feedback,
                "critic_report": critic_report,
                "original_parent_id": reviewer.parent_id,
                "prompt": format!("Debug and remediate reviewer rejection for task '{}'.", reviewer.title),
            }),
            trace_id: Some(reviewer.trace_id.clone()),
        })
        .await?;
    let remediation = task_service
        .set_status(&remediation.id, TaskStatus::InProgress)
        .await?;
    let generated_tasks = orchestrator.orchestrate(&remediation).await?;
    let generated_task_ids = generated_tasks
        .iter()
        .map(|task| task.id.clone())
        .collect::<Vec<_>>();
    log_run_event(
        audit_service.as_ref(),
        event_service.as_ref(),
        "remediation_plan_created",
        Some(&remediation),
        &remediation.project_id,
        Some(&remediation.trace_id),
        serde_json::json!({
            "run_id": run_id,
            "reviewer_task_id": reviewer.id,
            "generated_task_ids": generated_task_ids,
            "feedback": feedback,
            "failure_type": failure_type,
        }),
    )
    .await;
    let remediation = task_service
        .set_result(
            &remediation.id,
            serde_json::json!({
                "source": "remediation_orchestrator",
                "generated_task_ids": generated_task_ids,
            }),
        )
        .await?;
    let _ = task_service
        .set_status(&remediation.id, TaskStatus::Completed)
        .await?;
    Ok(generated_tasks)
}

async fn log_run_event(
    audit_service: Option<&Arc<dyn AuditLogService>>,
    event_service: Option<&Arc<dyn EventService>>,
    action: &str,
    task: Option<&Task>,
    project_id: &str,
    trace_id: Option<&str>,
    metadata: Value,
) {
    let trace_id = trace_id.unwrap_or_default();
    let mut metadata = metadata;
    metadata["trace_id"] = Value::String(trace_id.to_string());
    if let Some(event_service) = event_service {
        event_service.publish(Event::RunObserved {
            project_id: project_id.to_string(),
            task_id: task.map(|task| task.id.clone()),
            trace_id: trace_id.to_string(),
            action: action.to_string(),
            metadata: metadata.clone(),
        });
    }
    let Some(audit_service) = audit_service else {
        return;
    };
    let agent = task
        .and_then(|task| task.assigned_agent.clone())
        .unwrap_or_else(|| "runner".to_string());
    let mut entry = AuditLogEntry::new(agent, action)
        .project_id(project_id)
        .trace_id(trace_id)
        .level(AuditLogLevel::Info)
        .metadata(metadata);
    if let Some(task) = task {
        entry = entry.task_id(task.id.clone());
    }
    let _ = audit_service.log(entry).await;
}

async fn attach_upstream_artifacts(
    task_service: Arc<dyn TaskService>,
    audit_service: Option<&Arc<dyn AuditLogService>>,
    event_service: Option<&Arc<dyn EventService>>,
    run_id: &str,
    task: &mut Task,
) -> Result<ArtifactHandoffAttachResult, TauriCommandError> {
    let Some(parent_id) = task.parent_id.clone() else {
        return Ok(ArtifactHandoffAttachResult::default());
    };
    let mut upstream = task_service
        .list_by_project(&task.project_id)
        .await?
        .into_iter()
        .filter(|candidate| {
            candidate.parent_id.as_deref() == Some(parent_id.as_str())
                && candidate.id != task.id
                && candidate.status == TaskStatus::Completed
                && candidate.result.is_some()
        })
        .collect::<Vec<_>>();
    upstream.sort_by_key(|candidate| candidate.created_at);

    let recoveries = completed_artifact_handoff_recoveries(&upstream, task);
    let mut upstream_artifacts = Vec::new();
    let mut rejected_count = 0;
    for artifact in upstream {
        if is_artifact_handoff_remediation_for(task, &artifact) {
            upstream_artifacts.push(build_rejected_artifact_handoff_for_repair(task, &artifact));
            continue;
        }
        if recoveries.iter().any(|recovery| {
            recovery.payload["source_task_id"] == artifact.id
                && recovery.payload["target_task_id"] == task.id
        }) {
            continue;
        }
        match build_agent_artifact_handoff_result(&artifact) {
            Ok(handoff) => upstream_artifacts.push(handoff),
            Err(rejection) => {
                rejected_count += 1;
                log_artifact_handoff_rejection(
                    audit_service,
                    event_service,
                    run_id,
                    task,
                    &rejection,
                )
                .await;
                create_artifact_handoff_remediation_task(
                    task_service.clone(),
                    audit_service,
                    event_service,
                    run_id,
                    task,
                    &rejection,
                )
                .await?;
            }
        }
    }
    if is_reviewer_task(task) && task.payload.get("parent_result").is_none() {
        task.payload["parent_result"] = Value::Array(upstream_artifacts.clone());
    }
    task.payload["upstream_artifacts"] = Value::Array(upstream_artifacts);
    Ok(ArtifactHandoffAttachResult { rejected_count })
}

fn completed_artifact_handoff_recoveries(upstream: &[Task], target_task: &Task) -> Vec<Task> {
    upstream
        .iter()
        .filter(|task| {
            task.status == TaskStatus::Completed
                && task.result.is_some()
                && task.payload["source"] == "artifact_handoff_rejection"
                && task.payload["target_task_id"] == target_task.id
                && build_agent_artifact_handoff_result(task).is_ok()
        })
        .cloned()
        .collect()
}

fn is_artifact_handoff_remediation_for(target_task: &Task, source_task: &Task) -> bool {
    target_task.payload["source"] == "artifact_handoff_rejection"
        && target_task.payload["source_task_id"] == source_task.id
}

fn build_rejected_artifact_handoff_for_repair(repair_task: &Task, source_task: &Task) -> Value {
    let content = source_task
        .result
        .as_ref()
        .map(artifact_content_from_result)
        .unwrap_or(Value::Null);
    serde_json::json!({
        "schema_version": 1,
        "artifact_id": format!("artifact-{}-{}", source_task.id, source_task.iteration_count),
        "source_task_id": source_task.id,
        "source_agent": source_task.assigned_agent,
        "artifact_kind": repair_task.payload["artifact_kind"],
        "content": content,
        "task_id": source_task.id,
        "title": source_task.title,
        "kind": source_task.kind,
        "agent": source_task.assigned_agent,
        "result": source_task.result,
        "handoff_status": "rejected_for_repair",
        "rejection_reason": repair_task.payload["reason"],
        "repair_task_id": repair_task.id,
    })
}

#[derive(Debug, Clone, Default, PartialEq, Eq)]
struct ArtifactHandoffAttachResult {
    rejected_count: usize,
}

#[cfg(test)]
fn build_agent_artifact_handoff(task: &Task) -> Option<Value> {
    build_agent_artifact_handoff_result(task).ok()
}

fn build_agent_artifact_handoff_result(task: &Task) -> Result<Value, ArtifactHandoffRejection> {
    let envelope = build_agent_artifact_envelope(task).ok_or_else(|| ArtifactHandoffRejection {
        source_task_id: task.id.clone(),
        source_agent: task.assigned_agent.clone(),
        artifact_kind: artifact_kind_for_task(task),
        reason: "task has no result artifact".to_string(),
    })?;
    validate_agent_artifact_envelope(&envelope).map_err(|reason| ArtifactHandoffRejection {
        source_task_id: task.id.clone(),
        source_agent: task.assigned_agent.clone(),
        artifact_kind: envelope.artifact_kind.clone(),
        reason,
    })?;
    Ok(serde_json::json!({
        "schema_version": envelope.schema_version,
        "artifact_id": envelope.artifact_id,
        "source_task_id": envelope.source_task_id,
        "source_agent": envelope.source_agent,
        "artifact_kind": envelope.artifact_kind,
        "content": envelope.content,
        "task_id": task.id,
        "title": task.title,
        "kind": task.kind,
        "agent": task.assigned_agent,
        "result": task.result,
    }))
}

#[derive(Debug, Clone, PartialEq, Eq)]
struct ArtifactHandoffRejection {
    source_task_id: String,
    source_agent: Option<String>,
    artifact_kind: String,
    reason: String,
}

async fn log_artifact_handoff_rejection(
    audit_service: Option<&Arc<dyn AuditLogService>>,
    event_service: Option<&Arc<dyn EventService>>,
    run_id: &str,
    target_task: &Task,
    rejection: &ArtifactHandoffRejection,
) {
    log_run_event(
        audit_service,
        event_service,
        "artifact_handoff_rejected",
        Some(target_task),
        &target_task.project_id,
        Some(&target_task.trace_id),
        serde_json::json!({
            "run_id": run_id,
            "source_task_id": rejection.source_task_id,
            "source_agent": rejection.source_agent,
            "target_task_id": target_task.id,
            "target_agent": target_task.assigned_agent,
            "artifact_kind": rejection.artifact_kind,
            "reason": rejection.reason,
        }),
    )
    .await;
}

async fn create_artifact_handoff_remediation_task(
    task_service: Arc<dyn TaskService>,
    audit_service: Option<&Arc<dyn AuditLogService>>,
    event_service: Option<&Arc<dyn EventService>>,
    run_id: &str,
    target_task: &Task,
    rejection: &ArtifactHandoffRejection,
) -> Result<Task, TauriCommandError> {
    if let Some(existing) =
        find_existing_artifact_handoff_remediation(task_service.as_ref(), target_task, rejection)
            .await?
    {
        log_run_event(
            audit_service,
            event_service,
            "artifact_handoff_remediation_task_reused",
            Some(target_task),
            &target_task.project_id,
            Some(&target_task.trace_id),
            serde_json::json!({
                "run_id": run_id,
                "source_task_id": rejection.source_task_id,
                "target_task_id": target_task.id,
                "remediation_task_id": existing.id,
            }),
        )
        .await;
        return Ok(existing);
    }

    let remediation = task_service
        .submit(CreateTaskRequest {
            project_id: target_task.project_id.clone(),
            parent_id: target_task.parent_id.clone(),
            title: format!(
                "Fix invalid {} from {}",
                rejection.artifact_kind, rejection.source_task_id
            ),
            description: Some(rejection.reason.clone()),
            kind: "debug".to_string(),
            assigned_agent: rejection.source_agent.clone(),
            priority: target_task.priority + 1,
            payload: serde_json::json!({
                "source": "artifact_handoff_rejection",
                "source_task_id": rejection.source_task_id,
                "source_agent": rejection.source_agent,
                "target_task_id": target_task.id,
                "target_agent": target_task.assigned_agent,
                "artifact_kind": rejection.artifact_kind,
                "reason": rejection.reason,
                "prompt": format!(
                    "Fix the rejected {} produced by task {} so it satisfies the handoff schema before task {} reviews it.",
                    rejection.artifact_kind,
                    rejection.source_task_id,
                    target_task.id
                ),
            }),
            trace_id: Some(target_task.trace_id.clone()),
        })
        .await?;
    task_service
        .add_dependency(TaskDependency {
            task_id: target_task.id.clone(),
            depends_on: remediation.id.clone(),
            dep_type: "artifact_handoff_recovery".to_string(),
        })
        .await?;
    log_run_event(
        audit_service,
        event_service,
        "artifact_handoff_remediation_task_created",
        Some(&remediation),
        &remediation.project_id,
        Some(&remediation.trace_id),
        serde_json::json!({
            "run_id": run_id,
            "source_task_id": rejection.source_task_id,
            "target_task_id": target_task.id,
            "artifact_kind": rejection.artifact_kind,
            "reason": rejection.reason,
            "remediation_task_id": remediation.id,
        }),
    )
    .await;
    Ok(remediation)
}

async fn find_existing_artifact_handoff_remediation(
    task_service: &dyn TaskService,
    target_task: &Task,
    rejection: &ArtifactHandoffRejection,
) -> Result<Option<Task>, TauriCommandError> {
    Ok(task_service
        .list_by_project(&target_task.project_id)
        .await?
        .into_iter()
        .find(|task| {
            !task.status.is_terminal()
                && task.payload["source"] == "artifact_handoff_rejection"
                && task.payload["source_task_id"] == rejection.source_task_id
                && task.payload["target_task_id"] == target_task.id
        }))
}

fn build_agent_artifact_envelope(task: &Task) -> Option<AgentArtifactEnvelope> {
    let result = task.result.as_ref()?;
    build_agent_artifact_envelope_for(task, result)
}

fn build_agent_artifact_envelope_for(task: &Task, result: &Value) -> Option<AgentArtifactEnvelope> {
    Some(AgentArtifactEnvelope {
        schema_version: 1,
        artifact_id: format!("artifact-{}-{}", task.id, task.iteration_count),
        source_task_id: task.id.clone(),
        source_agent: task.assigned_agent.clone(),
        artifact_kind: artifact_kind_for_task(task),
        content: artifact_content_from_result(result),
    })
}

fn artifact_kind_for_task(task: &Task) -> String {
    match task.assigned_agent.as_deref() {
        Some("architect") => "design_artifact".to_string(),
        Some("coder") => "patch_artifact".to_string(),
        Some("qa") => "test_report_artifact".to_string(),
        Some("security") => "security_report_artifact".to_string(),
        Some("critic") => "review_decision".to_string(),
        Some(agent) => format!("{agent}_artifact"),
        None => format!("{}_artifact", task.kind),
    }
}

fn artifact_content_from_result(result: &Value) -> Value {
    result
        .pointer("/agent_result/artifact")
        .or_else(|| result.pointer("/artifact"))
        .or_else(|| result.pointer("/agent_result"))
        .cloned()
        .unwrap_or_else(|| result.clone())
}

fn validate_agent_artifact_envelope(envelope: &AgentArtifactEnvelope) -> Result<(), String> {
    if envelope.schema_version != 1 {
        return Err(format!(
            "unsupported artifact schema_version {}",
            envelope.schema_version
        ));
    }
    require_non_empty(&envelope.artifact_id, "artifact_id")?;
    require_non_empty(&envelope.source_task_id, "source_task_id")?;
    require_object(&envelope.content, "content")?;
    match envelope.artifact_kind.as_str() {
        "design_artifact" => validate_design_artifact(&envelope.content),
        "patch_artifact" => validate_patch_artifact(&envelope.content),
        "test_report_artifact" => validate_test_report_artifact(&envelope.content),
        "security_report_artifact" => validate_security_report_artifact(&envelope.content),
        "review_decision" => validate_review_decision_artifact(&envelope.content),
        _ => Ok(()),
    }
}

fn validate_design_artifact(content: &Value) -> Result<(), String> {
    require_text_field(content, "summary")
        .or_else(|_| require_text_field(content, "content"))
        .map(|_| ())
}

fn validate_patch_artifact(content: &Value) -> Result<(), String> {
    require_array_field(content, "files_changed")?;
    require_text_field(content, "summary")?;
    Ok(())
}

fn validate_test_report_artifact(content: &Value) -> Result<(), String> {
    require_text_field(content, "summary")
        .or_else(|_| require_text_field(content, "test_results"))
        .map(|_| ())
}

fn validate_security_report_artifact(content: &Value) -> Result<(), String> {
    require_text_field(content, "summary")
        .or_else(|_| require_text_field(content, "risk"))
        .map(|_| ())
}

fn validate_review_decision_artifact(content: &Value) -> Result<(), String> {
    require_text_field(content, "review_decision").map(|_| ())
}

fn require_object(value: &Value, field: &str) -> Result<(), String> {
    value
        .as_object()
        .map(|_| ())
        .ok_or_else(|| format!("{field} must be an object"))
}

fn require_non_empty(value: &str, field: &str) -> Result<(), String> {
    (!value.trim().is_empty())
        .then_some(())
        .ok_or_else(|| format!("{field} must be non-empty"))
}

fn require_text_field<'a>(content: &'a Value, field: &str) -> Result<&'a str, String> {
    content
        .get(field)
        .and_then(Value::as_str)
        .filter(|value| !value.trim().is_empty())
        .ok_or_else(|| format!("artifact content requires non-empty string field `{field}`"))
}

fn require_array_field<'a>(content: &'a Value, field: &str) -> Result<&'a Vec<Value>, String> {
    content
        .get(field)
        .and_then(Value::as_array)
        .filter(|value| !value.is_empty())
        .ok_or_else(|| format!("artifact content requires non-empty array field `{field}`"))
}

fn is_generated_chain_task(task: &Task) -> bool {
    task.parent_id.is_some()
}

fn is_reviewer_task(task: &Task) -> bool {
    task.kind == "review" || task.assigned_agent.as_deref() == Some("critic")
}

fn review_decision(task: &Task) -> Option<&str> {
    let result = task.result.as_ref()?;
    result
        .pointer("/agent_result/review_decision")
        .or_else(|| result.pointer("/agent_result/agent_result/review_decision"))
        .or_else(|| result.pointer("/review_decision"))
        .and_then(|value| value.as_str())
}

async fn handle_reviewer_rejection(
    task_service: Arc<dyn TaskService>,
    reviewer: &Task,
) -> Result<(), TauriCommandError> {
    let feedback = rejection_feedback(reviewer);

    let mut reviewer_for_retry = task_service
        .set_status(&reviewer.id, TaskStatus::Review)
        .await?;
    reviewer_for_retry.result = None;
    task_service.update_task(&reviewer_for_retry).await?;
    reviewer_for_retry = task_service
        .retry(&reviewer_for_retry.id, Some(feedback))
        .await?;

    if let Some(upstream) =
        rejected_upstream_target(task_service.clone(), reviewer, &reviewer_for_retry).await?
    {
        retry_upstream_chain_from(task_service, &upstream, &reviewer_for_retry, feedback).await?;
    }
    Ok(())
}

fn rejection_feedback(task: &Task) -> &str {
    task.result
        .as_ref()
        .and_then(|result| {
            result
                .pointer("/agent_result/feedback")
                .or_else(|| result.pointer("/agent_result/summary"))
                .or_else(|| result.pointer("/feedback"))
                .or_else(|| result.pointer("/summary"))
        })
        .and_then(|value| value.as_str())
        .unwrap_or("reviewer rejected the upstream artifact")
}

fn critic_report(task: &Task) -> Option<&Value> {
    let result = task.result.as_ref()?;
    result
        .pointer("/agent_result")
        .or_else(|| result.pointer("/agent_result/agent_result"))
        .or(Some(result))
}

fn rejection_target_task_id(task: &Task) -> Option<&str> {
    let result = task.result.as_ref()?;
    result
        .pointer("/agent_result/target_task_id")
        .or_else(|| result.pointer("/agent_result/agent_result/target_task_id"))
        .or_else(|| result.pointer("/target_task_id"))
        .and_then(|value| value.as_str())
}

async fn rejected_upstream_target(
    task_service: Arc<dyn TaskService>,
    reviewer: &Task,
    reviewer_for_retry: &Task,
) -> Result<Option<Task>, TauriCommandError> {
    if let Some(target_id) = rejection_target_task_id(reviewer)
        && let Some(target) = task_service.get(target_id).await?
        && target.project_id == reviewer.project_id
        && target.parent_id == reviewer.parent_id
        && target.id != reviewer.id
    {
        return Ok(Some(target));
    }
    latest_completed_upstream(task_service, reviewer_for_retry).await
}

async fn retry_upstream_chain_from(
    task_service: Arc<dyn TaskService>,
    target: &Task,
    reviewer: &Task,
    feedback: &str,
) -> Result<(), TauriCommandError> {
    let Some(parent_id) = target.parent_id.as_deref() else {
        return Ok(());
    };
    let mut siblings = task_service
        .list_by_project(&target.project_id)
        .await?
        .into_iter()
        .filter(|candidate| {
            candidate.parent_id.as_deref() == Some(parent_id)
                && candidate.id != reviewer.id
                && candidate.created_at >= target.created_at
                && matches!(
                    candidate.status,
                    TaskStatus::Completed | TaskStatus::Review | TaskStatus::Failed
                )
        })
        .collect::<Vec<_>>();
    siblings.sort_by_key(|candidate| candidate.created_at);

    for mut sibling in siblings {
        if sibling.status == TaskStatus::Completed {
            sibling.status = TaskStatus::Review;
        }
        sibling.result = None;
        sibling.started_at = None;
        sibling.finished_at = None;
        task_service.update_task(&sibling).await?;
        let _ = task_service.retry(&sibling.id, Some(feedback)).await?;
    }
    Ok(())
}

async fn latest_completed_upstream(
    task_service: Arc<dyn TaskService>,
    task: &Task,
) -> Result<Option<Task>, TauriCommandError> {
    let Some(parent_id) = task.parent_id.as_deref() else {
        return Ok(None);
    };
    let mut upstream = task_service
        .list_by_project(&task.project_id)
        .await?
        .into_iter()
        .filter(|candidate| {
            candidate.parent_id.as_deref() == Some(parent_id)
                && candidate.id != task.id
                && candidate.status == TaskStatus::Completed
        })
        .collect::<Vec<_>>();
    upstream.sort_by_key(|candidate| candidate.created_at);
    Ok(upstream.pop())
}

async fn project_ready_tasks(
    task_service: Arc<dyn TaskService>,
    project_id: &str,
) -> Result<Vec<Task>, TauriCommandError> {
    Ok(task_service
        .list_ready()
        .await?
        .into_iter()
        .filter(|task| task.project_id == project_id)
        .collect())
}

async fn next_project_ready_task(
    task_service: Arc<dyn TaskService>,
    project_id: &str,
) -> Result<Option<Task>, TauriCommandError> {
    let mut tasks = project_ready_tasks(task_service, project_id).await?;
    tasks.sort_by(|a, b| {
        b.priority_score
            .partial_cmp(&a.priority_score)
            .unwrap_or(std::cmp::Ordering::Equal)
            .then_with(|| a.created_at.cmp(&b.created_at))
    });
    Ok(tasks.into_iter().next())
}

