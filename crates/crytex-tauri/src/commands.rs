/// Errors returned by Tauri command handlers.
#[derive(Debug, thiserror::Error)]
pub enum TauriCommandError {
    #[error("bootstrap error: {0}")]
    Bootstrap(String),
    #[error("project error: {0}")]
    Project(ProjectError),
    #[error("task error: {0}")]
    Task(TaskError),
    #[error("orchestrator error: {0}")]
    Orchestrator(#[from] OrchestratorError),
    #[error("inference error: {0}")]
    Inference(#[from] InferenceError),
    #[error("agent service error: {0}")]
    AgentService(#[from] AgentServiceError),
    #[error("reward service error: {0}")]
    RewardService(#[from] RewardServiceError),
    #[error("state export error: {0}")]
    StateExport(#[from] StateExportError),
    #[error("indexer error: {0}")]
    Indexer(#[from] IndexerError),
    #[error("vector store error: {0}")]
    VectorStore(#[from] VectorStoreError),
    #[error("model manager error: {0}")]
    ModelManager(#[from] ModelManagerError),
    #[error("lora evolution error: {0}")]
    LoraEvolution(#[from] LoraEvolutionError),
}

impl From<ProjectError> for TauriCommandError {
    fn from(err: ProjectError) -> Self {
        Self::Project(err)
    }
}

impl From<TaskError> for TauriCommandError {
    fn from(err: TaskError) -> Self {
        Self::Task(err)
    }
}

