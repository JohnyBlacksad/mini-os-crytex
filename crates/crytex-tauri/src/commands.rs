/// Submit a high-level user goal and ask the orchestrator to generate tasks.
pub async fn submit_goal(
    task_service: Arc<dyn TaskService>,
    orchestrator: Arc<dyn Orchestrator>,
    request: SubmitGoalCommand,
) -> Result<GoalPlanResponse, TauriCommandError> {
    let goal = task_service
        .submit(CreateTaskRequest {
            project_id: request.project_id,
            parent_id: None,
            title: request.goal.clone(),
            description: Some(request.goal.clone()),
            kind: "codegen".to_string(),
            assigned_agent: Some("architect".to_string()),
            priority: 10,
            payload: serde_json::json!({
                "goal": request.goal,
                "prompt": request.goal,
                "context": request.context,
                "source": "user_goal"
            }),
            trace_id: request.trace_id,
        })
        .await?;
    let goal = task_service
        .set_status(&goal.id, TaskStatus::InProgress)
        .await?;
    let generated_tasks = orchestrator.orchestrate(&goal).await?;
    let mut held_tasks = Vec::with_capacity(generated_tasks.len());
    for task in generated_tasks {
        held_tasks.push(
            task_service
                .set_status(&task.id, TaskStatus::Backlog)
                .await?,
        );
    }
    let goal = task_service
        .set_status(&goal.id, TaskStatus::Review)
        .await?;

    Ok(GoalPlanResponse {
        goal,
        generated_tasks: held_tasks,
    })
}

async fn child_plan_tasks(
    task_service: Arc<dyn TaskService>,
    goal: &Task,
) -> Result<Vec<Task>, TauriCommandError> {
    Ok(task_service
        .list_by_project(&goal.project_id)
        .await?
        .into_iter()
        .filter(|task| task.parent_id.as_deref() == Some(goal.id.as_str()))
        .collect())
}

async fn get_goal_task(
    task_service: Arc<dyn TaskService>,
    goal_task_id: &str,
) -> Result<Task, TauriCommandError> {
    task_service
        .get(goal_task_id)
        .await?
        .ok_or_else(|| TauriCommandError::Task(TaskError::NotFound(goal_task_id.to_string())))
}

/// Approve an AI-generated plan and release its generated tasks for execution.
pub async fn approve_plan(
    task_service: Arc<dyn TaskService>,
    request: PlanDecisionCommand,
) -> Result<PlanDecisionResponse, TauriCommandError> {
    let goal = get_goal_task(task_service.clone(), &request.goal_task_id).await?;
    let generated_tasks = child_plan_tasks(task_service.clone(), &goal).await?;
    let mut released_tasks = Vec::with_capacity(generated_tasks.len());
    for task in generated_tasks {
        released_tasks.push(
            task_service
                .set_status(&task.id, TaskStatus::Pending)
                .await?,
        );
    }
    let goal = task_service.set_human_score(&goal.id, 1.0).await?;
    let goal = task_service
        .set_status(&goal.id, TaskStatus::Completed)
        .await?;

    Ok(PlanDecisionResponse {
        goal,
        generated_tasks: released_tasks,
    })
}

/// Reject an AI-generated plan, cancel its generated tasks, and return the goal for revision.
pub async fn reject_plan(
    task_service: Arc<dyn TaskService>,
    request: PlanDecisionCommand,
) -> Result<PlanDecisionResponse, TauriCommandError> {
    let goal = get_goal_task(task_service.clone(), &request.goal_task_id).await?;
    let generated_tasks = child_plan_tasks(task_service.clone(), &goal).await?;
    let mut cancelled_tasks = Vec::with_capacity(generated_tasks.len());
    for task in generated_tasks {
        cancelled_tasks.push(
            task_service
                .set_status(&task.id, TaskStatus::Cancelled)
                .await?,
        );
    }
    let goal = task_service.set_human_score(&goal.id, 0.0).await?;
    let goal = task_service
        .retry(&goal.id, request.comment.as_deref())
        .await?;

    Ok(PlanDecisionResponse {
        goal,
        generated_tasks: cancelled_tasks,
    })
}

